package com.enterat.bda;

public class Curso {

	//Atributos de clase
	private int id_curso;
	private String curso;
	
	//Getters and Setters
	public int getId_curso() {
		return id_curso;
	}
	public void setId_curso(int id_curso) {
		this.id_curso = id_curso;
	}
	public String getCurso() {
		return curso;
	}
	public void setCurso(String curso) {
		this.curso = curso;
	}
	
}